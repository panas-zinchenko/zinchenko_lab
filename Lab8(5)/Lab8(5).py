import pickle  # Імпортував модуль pickle
a = {1: 'red', 2: 'blue', 3: 'green'}  # Створив словник

f = open('005.txt', 'wb')  # Відкрив для запису текстовий файл у бвійковому режимі
pickle.dump(a, f)  # Зберіг словник 'а' у файл 'f'
f.close()
f = open('005.txt', 'rb')  # Відкрив для читання текстовий файл у бвійковому режимі
b = pickle.load(f)  # Завантажив зміст файла f
print(b)
f.close()